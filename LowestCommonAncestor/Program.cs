        static TreeNode[] TreeFactory(int[,] matrix)
        {
            byte asciiSymbol = 65;
            TreeNode[] arr = new TreeNode[matrix.GetLength(0)];
            for (int i = 0; i < arr.Length; i++)
            {
                TreeNode node = new TreeNode(asciiSymbol++);
                arr[i] = node;
            }

            for (int i = 0; i < matrix.GetLength(0); i++)
            {
                for (int j = 0; j < matrix.GetLength(1); j++)
                {
                    if (matrix[i, j] == 1)
                    {
                        arr[i].Children.Add(arr[j]);
                    }
                }
            }
            return arr;
        }

        public static void EvalMatrix(int[,] matrix, int[] b1Pairs, int[] b2Pairs)
        {
            Console.WriteLine("\r\n");
            for (int i = 0; i < matrix.GetLength(0); i++)
            {
                for (int j = 0; j < matrix.GetLength(1); j++)
                {
                    Console.Write(" {0}", matrix[i, j]);
                }
                Console.WriteLine();
            }
            Console.WriteLine("\r\n");

            TreeNode[] arr = TreeFactory(matrix);
            TreeNode root = arr[0];

            LCASolution.PreProcessTree(root);
            
            //eval diferent pairs
            for (int i = 0; i < b1Pairs.Length; i++)
            {
                TreeNode b1 = arr[b1Pairs[i]];
                TreeNode b2 = arr[b2Pairs[i]];

                var LCA = LCASolution.linearLCA(b1, b2);
                Console.WriteLine("Lowest Common Ancestor of {0} and {1} is {2}", 
                    (char)b1.Symbol, (char)b2.Symbol, (char)LCA.Symbol);
            }

            Console.WriteLine();
        }

